class MethodPrintout : public Printout
{
  typedef Printout Inherited;
 protected:
  Method *pMethod;
  int hLine;
  int LinesPerPage;
  int NumLines;
 public:
  MethodPrintout(LPCSTR ATitle, Method *_pMethod) : Printout(ATitle), pMethod(_pMethod) {}
  virtual void SetPrintParams(DC *_pdc, WPoint pageSize);
  virtual BOOL HasPage(int pageNumber)
  {
    return (pageNumber-1)*LinesPerPage<NumLines;
  }
  virtual void PrintPage(int page, WRect& rect, unsigned flags);
};

void MethodPrintout::SetPrintParams(DC *_pdc, WPoint pageSize)
{
  Inherited::SetPrintParams(_pdc,pageSize);
  hLine=pdc->GetTextExtent("H").Y();
  if(!hLine || (LinesPerPage=PageSize.Y()/hLine)==0) LinesPerPage=1;
  NumLines = 0;
  for(ControlInfo *cip=MethodTableControlInfo; cip->type!=citNULL; cip++) {
    NumLines++;
  }
}


int LoadDATAText( HINSTANCE hInstance, LPCSTR lpszName, LPSTR lpszBuffer, int cbBuffer )
{
  if( lpszBuffer == 0 || cbBuffer == 0 ) return 0;

  HRSRC hrsrc = FindResource( hInstance, lpszName, "DATA" );
  if( ! hrsrc ) return 0;

  HGLOBAL hmem = LoadResource( hInstance, hrsrc );
  LPSTR str = (LPSTR) LockResource( hmem );
  if( ! str ) return 0;

  int size = Min( (int)SizeofResource( hInstance, hrsrc ), cbBuffer );

  //lstrcpyn( lpszBuffer, str, size );
  LPSTR out = lpszBuffer;
  for( int j = 0; j < size; str++, j++ ) {
    switch( *str ) {
      default: *out++ = *str;
    }
  }
  *out = '\0';

  UnlockResource( hmem );
  FreeResource( hmem );

  return size;
}

