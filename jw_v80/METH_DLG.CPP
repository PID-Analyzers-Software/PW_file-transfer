void PointsAndAlarmsDlg::SetupControls(void *data)
{
  Inherited::SetupControls(data);

  const Method *pMethod=(Method *)data;
  {
    #if POINTS()<2
    ulong points=1; // always 1 point
    #else
    const ulong &points=pMethod->method_table.points;
    #endif
    int id=IDD_POINT1;
    for(ulong m=1; id<=IDD_POINTS+POINTS(); id++, m<<=1) {
      CheckDlgButton(HWindow,id, (points&m)?1:0);
    }
    for(; id<=IDD_POINTS+MAX_POINTS; id++) {
      ShowWindow(GetDlgItem(HWindow,id),SW_HIDE);
    }
  }
  {
    const ulong &lalarms=pMethod->method_table.lalarms;
    int id=IDD_LA1;
#if !NO_ALARMS()  // cond. added. P.P. 05/03/11
    for(ulong m=1; id<=IDD_LOW_ALARMS+POINTS(); id++, m<<=1) {
      CheckDlgButton(HWindow,id, (lalarms&m)?1:0);
    }
#else
    for(ulong m=1; id<=IDD_LOW_ALARMS+POINTS(); id++, m<<=1) {
      ShowWindow(GetDlgItem(HWindow,id),SW_HIDE);
    }
#endif
    for(; id<=IDD_LOW_ALARMS+MAX_POINTS; id++) {
      ShowWindow(GetDlgItem(HWindow,id),SW_HIDE);
    }
  }
  {
    const ulong &halarms=pMethod->method_table.halarms;
    int id=IDD_HA1;
#if !NO_ALARMS()  // cond. added. P.P. 05/03/11
    for(ulong m=1; id<=IDD_HIGH_ALARMS+POINTS(); id++, m<<=1) {
      CheckDlgButton(HWindow,id, (halarms&m)?1:0);
    }
#else
    for(ulong m=1; id<=IDD_HIGH_ALARMS+POINTS(); id++, m<<=1) {
      ShowWindow(GetDlgItem(HWindow,id),SW_HIDE);
    }
#endif
    for(; id<=IDD_HIGH_ALARMS+MAX_POINTS; id++) {
      ShowWindow(GetDlgItem(HWindow,id),SW_HIDE);
    }
  }
  #if POINTS()<2
  {
    ShowWindow(GetDlgItem(HWindow,IDD_POINT1),SW_HIDE);
    ShowWindow(GetDlgItem(HWindow,IDD_LA1), SW_SHOW);
    ShowWindow(GetDlgItem(HWindow,IDD_HA1), SW_SHOW);

    ShowWindow(GetDlgItem(HWindow,IDD_POINTS),SW_HIDE);
    ShowWindow(GetDlgItem(HWindow,IDD_LOW_ALARMS),SW_HIDE);
    ShowWindow(GetDlgItem(HWindow,IDD_HIGH_ALARMS),SW_HIDE);

    SetDlgItemText(HWindow,IDD_LA1,GetDlgItemText(HWindow, IDD_LOW_ALARMS));
    SetDlgItemText(HWindow,IDD_HA1,GetDlgItemText(HWindow, IDD_HIGH_ALARMS));

    RECT rc;
    GetWindowRect(GetDlgItem(HWindow,IDD_LA1),&rc);
    int height=rc.bottom-rc.top;
    GetWindowRect(GetDlgItem(HWindow,IDD_LOW_ALARMS),&rc);
    MapWindowPoints(0, HWindow,(LPPOINT)&rc,2);
    SetWindowPos(GetDlgItem(HWindow,IDD_LA1), 0, rc.left, rc.top, rc.right-rc.left, height, SWP_NOACTIVATE|SWP_NOZORDER);
    GetWindowRect(GetDlgItem(HWindow,IDD_HIGH_ALARMS),&rc);
    MapWindowPoints(0, HWindow,(LPPOINT)&rc,2);
    SetWindowPos(GetDlgItem(HWindow,IDD_HA1), 0, rc.left, rc.top, rc.right-rc.left, height, SWP_NOACTIVATE|SWP_NOZORDER);
  }
  #endif
  ReflectAlarms();
}

void PointsAndAlarmsDlg::AcceptControls(void *data)
{
  Inherited::AcceptControls(data);
  Method *pMethod=(Method *)data;
  {
    ulong &points=pMethod->method_table.points;
    points=0;
    int id=IDD_POINT1;
    for(ulong m=1; id<=IDD_POINTS+POINTS(); id++, m<<=1) {
      if(IsDlgButtonChecked(HWindow,id)) points|=m;
    }
    if(!points) points=1; // set first bit
  }
  {
    ulong &lalarms=pMethod->method_table.lalarms;
    lalarms=0;
    int id=IDD_LA1;
    for(ulong m=1; id<=IDD_LOW_ALARMS+POINTS(); id++, m<<=1) {
      if(IsDlgButtonChecked(HWindow,id)) lalarms|=m;
    }
  }
  {
    ulong &halarms=pMethod->method_table.halarms;
    halarms=0;
    int id=IDD_HA1;
    for(ulong m=1; id<=IDD_HIGH_ALARMS+POINTS(); id++, m<<=1) {
      if(IsDlgButtonChecked(HWindow,id)) halarms|=m;
    }
  }
}

void PointsAndAlarmsDlg::ReflectAlarms()
{
#if !NO_ALARMS()  // cond. added. P.P. 05/03/11
  int b=IsDlgButtonChecked(HWindow, IDD_ALARMS);
#else
  int b = 0; // added. P.P. 05/03/11
#endif
  {
    EnableWindow(GetDlgItem(HWindow,IDD_LOW_ALARMS),b);
    for(int id=IDD_LA1; id<=IDD_LOW_ALARMS+POINTS(); id++) {
      int b1=b&&IsDlgButtonChecked(HWindow,IDD_POINTS+(id-IDD_LOW_ALARMS));
      EnableWindow(GetDlgItem(HWindow,id),b1);
    }
  }
  {
    EnableWindow(GetDlgItem(HWindow,IDD_HIGH_ALARMS),b);
    for(int id=IDD_HA1; id<=IDD_HIGH_ALARMS+POINTS(); id++) {
      int b1=b&&IsDlgButtonChecked(HWindow,IDD_POINTS+(id-IDD_HIGH_ALARMS));
      EnableWindow(GetDlgItem(HWindow,id),b1);
    }
  }
}
