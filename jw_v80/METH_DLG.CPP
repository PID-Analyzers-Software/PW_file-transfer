class StrNode: public List::Node, public Str // meth_func.cpp
{
 public:
  StrNode(const char *str=0);
};
void StrPrintMethod(Method *pMethod, List *list)
{
  char str[1024];
  LoadDATAText( GetApplicationObject()->hInstance, "MethodPrintFormat", str, sizeof(str) );

  char *cp=str;

  //int l=0;
  for(int i=0; /*i<NumLines &&*/ i< 11111; i++) {
    char prn[256],*p=prn;
    for( ; *cp; cp++) {
      switch(*cp) {
        case 12: return;
        default:
          *p++=*cp;
        break;
        case '$':
        {
          cp++;
          int id=*(((int *)cp)); cp++;
          for(ControlInfo *cip=MethodTableControlInfo; cip->type!=citNULL; cip++) {
            if(id==cip->idControl) {
              char buff[256];
              void *data=(char *)pMethod + cip->offset;
              switch(cip->type) {
                case citINT:
                  wsprintf(buff,"%d", *(int *)data);
                break;
                case citLONG:
                  wsprintf(buff,"%d", *(long *)data);
                break;
                case citDouble:
                  wsprintf(buff,"%f", *(double *)data);
                break;
                case citTEMP:
                  wsprintf(buff,"%u", *(unsigned *)data);
                break;
                case citTIME:
                {
                  long l=*(long *)data;
                  char str[12];
                  wsprintf(str, "%ld:%.2d",l/60,(int)(l%60));
                  wsprintf(buff,"%s",str);
                }
                break;
                case citONOFF:
                  wsprintf(buff,"%s", *(int *)data ? "On": "Off");
		break;
                case citOFFON:
                  wsprintf(buff,"%d", *(int *)data ? "Off": "On");
                break;
                case citRadio:
                {
                  for(int i=0; i<(int)cip->info1; i++) {
                    if((*(int *)data)==i) wsprintf(buff,"%d",i);
                  }
                }
                break;
                case citComboList:
                  wsprintf(buff,"%d", *(int *)data);
                break;
                case citCYCLE:
                  wsprintf(buff,"%d", *(int *)data);
                break;
                case citStr:
                  wsprintf(buff,"%s", (LPSTR)((Str *)data)->Get() );
                break;
                default:
                  *buff=0;
                break;
              }
              lstrcpy(p,buff); p+=lstrlen(buff);
              break;
            }
          }
        }
      }
    }
    cp++;
    *p=0;

    if(!pMethod->method_table.RampingEnabled && 3<=i && i<6) continue;
    list->append(new StrNode(prn));

    //l++;
  }
}

void PrintMethod(int page,int LinesPerPage,Method *pMethod,DC *pdc,WRect &rect)
{
  List *list = new List;
  StrPrintMethod(pMethod, list);

  int nTabPositions[]={2,4,6};
  const int cTabStops=sizeof(nTabPositions)/sizeof(nTabPositions[0]);
  {  // convert inches to pixels
    int resX=pdc->GetCapability(LOGPIXELSX);
    for(int i=0; i<cTabStops; i++) nTabPositions[i]*=resX;
  }

  int l=0;
  int hLine=pdc->GetTextExtent("H").Y();
  StrNode *prn=(StrNode *)list->first();
  for(int i=(page-1)*LinesPerPage; /*i<NumLines &&*/ i< page*LinesPerPage; i++) {
    if(!prn) break;
    pdc->TabbedTextOut(rect.Left(),rect.Top()+hLine*l,*prn,lstrlen(*prn), cTabStops, nTabPositions, rect.Left());
    l++;
    prn=(StrNode *)prn->nextNode();
  }
  destroy(list);
}

